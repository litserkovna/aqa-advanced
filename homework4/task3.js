const number = 7;
console.log('Here is the calculation for number', number);
for (let a = 1; a <= 10; a++) {
  console.log(`${number} * ${a} = ${number * a}`);
}
const secondnumber = 8;
console.log('Here is the calculation for number', secondnumber);
let b = 1;
while (b <= 10) {
  console.log(`${secondnumber} * ${b} = ${secondnumber * b}`);
  b++;
}
